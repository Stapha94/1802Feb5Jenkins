package QFifteen;

public class Functionality implements Interfaces{

    public int add(int x, int y){

        return x+y;

    }

    public int subtract(int x, int y){

        return x-y;

    }

    public int multiply(int x, int y){

        return x*y;

    }

    public float divide(float x, float y){

        return x/y;

    }

}
